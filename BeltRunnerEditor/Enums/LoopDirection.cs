namespace BeltRunnerEditor.Enums
{
    /// <summary>
    /// Enumeration of supported loop directions
    /// </summary>
    public enum LoopDirection
    {
        /// <summary>
        /// Unknown direction
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Move forward in the loop
        /// </summary>
        Forward = 1,

        /// <summary>
        /// Bounce forward and backward in the loop
        /// </summary>
        Bounce = 2
    }
}
